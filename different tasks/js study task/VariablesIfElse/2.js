/**
 * Declare a variable for storing a Number.  
If variable less than 21, log ‘Do not sell alcohol’ to the console. 
If variable more or equal to 21, log ‘You can sell the alcohol’ to the console. 
 */

const age = 22;

if (age >= 21) {
    console.log("You can sell the alcohol");
} else if (age < 21) {
    console.log("Do not sell alcohol");
}