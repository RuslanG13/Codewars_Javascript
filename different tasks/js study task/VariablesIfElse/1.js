/**
 * Declare a variable for storing Boolean data.  
If variable equal to true, log ‘Test passed’ to the console. 
If variable equal to false, log ‘Test failed’ to the console. 
If variable equal something else, log ‘Unknown error’ to the console.
 */

const isBoolean = true;

if (isBoolean === true) {
    console.log("Test passed");
} else if (isBoolean === false) {
    console.log("Test failed");
} else {
    console.log("Unknown error");
}

